
public class CaracteresEspeciales {
    
    public static void main(String arg[]){
    
        var nombre = "Pablo";
        System.out.println("salto de linea:  = \n " + nombre );
        System.out.println("Tabular: \t" + nombre);
        System.out.println("Retroceder 1 lugar: \b" + nombre);
        System.out.println("Retroceder 2 lugares: \b\b" + nombre);
        System.out.println("Cmilla simple: \'" + nombre + "\'");
        System.out.println("Cmilla doble: \"" + nombre + "\"");
        
    }
    
}

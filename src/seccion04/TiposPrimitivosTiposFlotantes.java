
public class TiposPrimitivosTiposFlotantes {

    public static void main(String arg[]){
    
        /*
            Tipos primitivos de tipo flotante
            FLOAT
            DOUBLE
        */
    
        float numeroFloat = (float)10.0;
        System.out.println("numeroFloat = " + numeroFloat);
        System.out.println("(float)10.0 = " + 10.0F);
        System.out.println("Minimo adminitible en Float: " + Float.MIN_VALUE);
        System.out.println("Maximo adminitible en Float: " + Float.MAX_VALUE);
        
        System.out.println("");
        
    
        double numeroDouble = 1.7976931348623157E308;
        System.out.println("numeroDouble = " + numeroDouble);
        System.out.println("(double)1.7976931348623157E308");
        System.out.println("Minimo adminitible en Double: " + Double.MIN_VALUE);
        System.out.println("Maximo adminitible en Double: " + Double.MAX_VALUE);
        
        System.out.println("");
        
    }
    
}
